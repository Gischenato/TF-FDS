CREATE TABLE "Ponto" (
  "ponto_ID" int,
  "latitude" float,
  "longitude" float,
  PRIMARY KEY ("ponto_ID")
);

CREATE TABLE "Aeroporto" (
  "artoporto_ID" int,
  "ponto_ID" int,
  "nome" text,
  "cidade" text,
  PRIMARY KEY ("artoporto_ID")
);

CREATE TABLE "Rota" (
  "rota_ID" int,
  "aeroporto_origem" int,
  "aeroporto_destino" int,
  PRIMARY KEY ("rota_ID"),
  CONSTRAINT "FK_Rota.aeroporto_destino"
    FOREIGN KEY ("aeroporto_destino")
      REFERENCES "Aeroporto"("artoporto_ID")
);

CREATE TABLE "Voo" (
  "voo_ID" int,
  "rota_ID" int,
  "aviao_ID" int,
  "data" Date,
  PRIMARY KEY ("voo_ID"),
  CONSTRAINT "FK_Voo.rota_ID"
    FOREIGN KEY ("rota_ID")
      REFERENCES "Rota"("rota_ID")
);

CREATE TABLE "Aerovia" (
  "aerovia_ID" int,
  "ponto_origem" int,
  "ponto_destino" int,
  PRIMARY KEY ("aerovia_ID"),
  CONSTRAINT "FK_Aerovia.ponto_destino"
    FOREIGN KEY ("ponto_destino")
      REFERENCES "Ponto"("ponto_ID")
);

CREATE TABLE "Aviao" (
  "aviao_ID" int,
  "nome" text,
  "codigo" int,
  "empresa" text,
  PRIMARY KEY ("aviao_ID")
);

CREATE TABLE "Aluguel" (
  "aluguel_ID" int,
  "aerovia_ID" int,
  "voo_ID" int,
  "altitude" int,
  "horario" Date,
  PRIMARY KEY ("aluguel_ID"),
  CONSTRAINT "FK_Aluguel.aerovia_ID"
    FOREIGN KEY ("aerovia_ID")
      REFERENCES "Aerovia"("aerovia_ID")
);

CREATE TABLE "Aerovia_Rota" (
  "aerovia_rota_ID" int,
  "aerovia_ID" int,
  "rota_ID" int,
  PRIMARY KEY ("aerovia_rota_ID"),
  CONSTRAINT "FK_Aerovia_Rota.aerovia_ID"
    FOREIGN KEY ("aerovia_ID")
      REFERENCES "Aerovia"("ponto_destino")
);


